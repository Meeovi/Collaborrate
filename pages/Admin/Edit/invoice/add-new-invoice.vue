        this.shipping_information = ' ';
        this.shipping_and_handling = ' ';
        this.payment_method = ' ';
        this.bill_to_name = ' ';
        this.customer_group = ' ';
        this.grand_total_purchased = ' ';
        this.grand_total_base = ' ';
        this.email = ' ';
        this.billing_address = ' ';
        this.shipping_address = ' ';
      },
    },
    apollo: {
